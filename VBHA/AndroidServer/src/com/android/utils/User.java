package com.android.utils;

public class User {
	public String name;
	public String pwd;
	public int age;
	public String email;
	public User(){
		
	}
	
	public User(String name, String pwd, int age, String email) {
		super();
		this.name = name;
		this.pwd = pwd;
		this.age = age;
		this.email = email;
	}
	
	

}
